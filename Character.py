    def __init__(self,name,hp,level,xp,strength,dexterity,constitution,intelligence,wisdom,charisma):
        self.name = name
        self.hp = hp
        self.level = level
        self.xp = xp
        self.strength = strength
        self.dexterity = dexterity
        self.constitution = constitution
        self.intelligence = intelligence
        self.wisdom = wisdom
        self.charisma = charisma
